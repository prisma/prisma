import { describe, expect, test } from 'vitest'

import { parseWKB } from '../wkb-parser'

describe('WKB Parser', () => {
  describe('Point', () => {
    test('parses Point without SRID', () => {
      const wkb = new Uint8Array([
        0x01, 0x01, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0xf0, 0x3f, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x40,
      ])

      expect(parseWKB(wkb)).toEqual({
        type: 'Point',
        coordinates: [1, 2],
      })
    })

    test('parses Point with SRID 4326', () => {
      const wkb = new Uint8Array([
        0x01, 0x01, 0x00, 0x00, 0x20, 0xe6, 0x10, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0xf0, 0x3f, 0x00,
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x40,
      ])

      expect(parseWKB(wkb)).toEqual({
        type: 'Point',
        coordinates: [1, 2],
        srid: 4326,
      })
    })

    test('parses Point with negative coordinates', () => {
      const wkb = new Uint8Array([
        0x01, 0x01, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0xf0, 0xbf, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x00, 0x00, 0xc0,
      ])

      expect(parseWKB(wkb)).toEqual({
        type: 'Point',
        coordinates: [-1, -2],
      })
    })

    test('parses Point with decimal coordinates', () => {
      const wkb = new Uint8Array([
        0x01, 0x01, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x80, 0xf8, 0x3f, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x00, 0x04, 0x40,
      ])

      const result = parseWKB(wkb)
      expect(result.type).toBe('Point')
      expect(result.coordinates[0]).toBeCloseTo(1.53125, 5)
      expect(result.coordinates[1]).toBeCloseTo(2.5, 5)
    })
  })

  describe('LineString', () => {
    test('parses LineString without SRID', () => {
      const wkb = new Uint8Array([
        0x01, 0x02, 0x00, 0x00, 0x00, 0x02, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0xf0, 0x3f, 0x00,
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x40, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x08, 0x40, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x00, 0x10, 0x40,
      ])

      expect(parseWKB(wkb)).toEqual({
        type: 'LineString',
        coordinates: [
          [1, 2],
          [3, 4],
        ],
      })
    })

    test('parses LineString with SRID 4326', () => {
      const wkb = new Uint8Array([
        0x01, 0x02, 0x00, 0x00, 0x20, 0xe6, 0x10, 0x00, 0x00, 0x03, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0xf0,
        0x3f, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0xf0, 0x3f, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x40, 0x00,
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x40,
      ])

      expect(parseWKB(wkb)).toEqual({
        type: 'LineString',
        coordinates: [
          [0, 0],
          [1, 1],
          [2, 2],
        ],
        srid: 4326,
      })
    })
  })

  describe('Polygon', () => {
    test('parses Polygon without holes', () => {
      const wkb = new Uint8Array([
        0x01, 0x03, 0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x04, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0xf0,
        0x3f, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0xf0, 0x3f, 0x00,
        0x00, 0x00, 0x00, 0x00, 0x00, 0xf0, 0x3f, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x00, 0x00, 0x00,
      ])

      expect(parseWKB(wkb)).toEqual({
        type: 'Polygon',
        coordinates: [
          [
            [0, 0],
            [1, 0],
            [1, 1],
            [0, 0],
          ],
        ],
      })
    })

    test('parses Polygon with SRID and hole', () => {
      const wkb = new Uint8Array([
        0x01, 0x03, 0x00, 0x00, 0x20, 0xe6, 0x10, 0x00, 0x00, 0x02, 0x00, 0x00, 0x00, 0x05, 0x00, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x00, 0x14, 0x40, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x00, 0x14, 0x40, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x14, 0x40, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x14, 0x40, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x04, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0xf0,
        0x3f, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0xf0, 0x3f, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x40, 0x00,
        0x00, 0x00, 0x00, 0x00, 0x00, 0xf0, 0x3f, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x40, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x00, 0x00, 0x40, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0xf0, 0x3f, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x00, 0xf0, 0x3f,
      ])

      expect(parseWKB(wkb)).toEqual({
        type: 'Polygon',
        coordinates: [
          [
            [0, 0],
            [5, 0],
            [5, 5],
            [0, 5],
            [0, 0],
          ],
          [
            [1, 1],
            [2, 1],
            [2, 2],
            [1, 1],
          ],
        ],
        srid: 4326,
      })
    })
  })

  describe('Error handling', () => {
    test('throws on unsupported geometry type', () => {
      const wkb = new Uint8Array([0x01, 0x04, 0x00, 0x00, 0x00])

      expect(() => parseWKB(wkb)).toThrow('Unsupported WKB geometry type: 4')
    })

    test('throws on invalid buffer', () => {
      const wkb = new Uint8Array([0x01])

      expect(() => parseWKB(wkb)).toThrow()
    })
  })

  describe('Big endian', () => {
    test('parses Point in big endian', () => {
      const wkb = new Uint8Array([
        0x00, 0x00, 0x00, 0x00, 0x01, 0x3f, 0xf0, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x40, 0x00, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x00,
      ])

      expect(parseWKB(wkb)).toEqual({
        type: 'Point',
        coordinates: [1, 2],
      })
    })
  })
})
